				<p class="description text-justify">
					BORRADOR DE PAPA CORRECTORES es una iniciativa académica desarrollada por Pierre Chan y José Paolillo (estudiante y bachiller de la PUCP, respectivamente), que se ocupa de la corrección de estilo.<br> 
					<br>
					Dicha iniciativa se inscribe en un contexto nacional caracterizado, entre otras cosas, por la pérdida total o parcial del arte de la escritura y por el menosprecio de quienes lo practican. Esta realidad es más evidente entre los jóvenes, lo cual responde a múltiples factores, como la decadencia generalizada del  hábito de la lectura, el escaso tiempo dedicado a interiorizar la gramática del idioma o, en otros casos, a la complejidad de las ideas por transmitirse, que rebasa nuestra capacidad para comunicarlas exitosamente a través de la escritura.<br>
					<br>
					En concreto, carecer de dichos conocimientos resulta más inconveniente de lo que aparenta, puesto que, en diversas ocasiones, requerimos de una escritura clara para optar por un grado académico, para conceder autoridad a nuestras publicaciones (académicas, virtuales, etc.) o, sencillamente, para comunicarnos. En relación con este último aspecto, recordemos que la variante estándar de nuestra lengua, que se manifiesta principalmente en la escritura,  facilita la comunicación entre hablantes del mismo idioma (por más absurdo que esto parezca), pues la lengua oral abunda en regionalismos, dialectos, diferencias generacionales o socioeconómicas, que podrían entorpecer la comunicación. <br>
					<br>	
					Ante dicho escenario, BORRADOR DE PAPA CORRECTORES se propone aliviar las carencias mencionadas: por un lado, existe la consigna de mejorar la calidad de los textos escritos, de tal modo que se reduzca la dificultad que envuelve su comprensión; y, por otro lado, se pretende crear una oferta laboral favorable a aquellas personas con dotes para la escritura.<br><br>
